    private static List<Pair> foldAlongHorizontal(List<Pair> dotts, Integer line) {
        Set<Pair> result = new HashSet<>();

        for(Pair dott : dotts) {
            if(dott.y <= line) {
                result.add(dott);
            } else {
                result.add(new Pair(dott.x, line - (dott.y - line)));
            }
        }
        return result.stream().collect(Collectors.toList());
    }

    private static List<Pair> foldAlongVertical(List<Pair> dotts, Integer line) {
        Set<Pair> result = new HashSet<>();

        for(Pair dott : dotts) {
            if(dott.x <= line) {
                result.add(dott);
            } else {
                result.add(new Pair(line - (dott.x - line), dott.y));
            }
        }
        return result.stream().collect(Collectors.toList());
    }

    private static void printDotts(List<Pair> dotts) {
        int minX= dotts.stream().map(e -> e.x).min(Comparator.naturalOrder()).get();
        int maxX= dotts.stream().map(e -> e.x).max(Comparator.naturalOrder()).get();
        int minY= dotts.stream().map(e -> e.y).min(Comparator.naturalOrder()).get();
        int maxY= dotts.stream().map(e -> e.y).max(Comparator.naturalOrder()).get();

        for(int i=minY; i<=maxY;i++) {
            for(int j=minX; j<=maxX; j++) {
                if(dotts.contains(new Pair(j, i)) ){
                    System.out.print("#");
                } else {
                    System.out.print(".");
                }
            }
            System.out.println();
        }
        System.out.println();

    }

    private static List<Pair> getDotts(String testInput) {
        List<Pair> result = new ArrayList<>();

        for(String line : testInput.split("\n") ) {
            if(line.contains("fold") || line.trim().isEmpty()) continue;

            result.add(new Pair(Integer.parseInt(line.split(",")[0].trim()), Integer.parseInt(line.split(",")[1].trim())));
        }

        return result;
    }

    private static Integer getNumberOfPaths(String node, Map<String, List<String>> connections, Set<String> visited) {
        int nb = 0;
        if(node.equals("end")) return 1;

        if(node.toLowerCase().equals(node)) {
            visited.add(node);
        }

        for(String nextNode : connections.get(node)) {
            if(visited.contains(nextNode)) continue;

            nb += getNumberOfPaths(nextNode, connections, new HashSet<>(visited));
        }

        return nb;

    }


