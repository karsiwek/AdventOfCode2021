    static List<String> types = Arrays.asList("(", "[", "{", "<");
    static Map<String, String> closings = Map.of("(",")","[","]","{","}","<",">");
    static Map<String, Long> scoring = Map.of(")",1l,"]",2l,"}",3l,">", 4l);



    public static void main(String[] args) {
        List<Long> scores = new ArrayList<>();
        for(String line : mainInput.split("\n")) {
            try {
                List<String> input = new ArrayList<>(Arrays.asList(line.trim().split("")));
                while (!input.isEmpty()) {
                    parseLine(input);
                }

            } catch (IllegalStateException e) {
                scores.add(getScore(e.getMessage()));
            } catch (IllegalArgumentException e) {
                continue;
            }

        }

        System.out.println(scores.stream().sorted().collect(Collectors.toList()).get(scores.size()/2));
//not 212712187
        //ok 1952146692
    }

    private static Long getScore(String message) {
        Long score = 0l;

        for(String elem : message.split("")) {
            score *= 5;
            score += scoring.get(elem);
        }

        return score;
    }

    static Chunk parseLine(List<String> input) {

        if(input.isEmpty()) return null;

        if(!types.contains(input.get(0))) {
            throw new IllegalArgumentException(input.get(0));
        }
        
        String type = input.get(0);
        Chunk chunk = new Chunk(new ArrayList<>(), type);
        input.remove(0);

        while(true) {
            if(input.isEmpty()) {
                throw new IllegalStateException(closings.get(type));
            }
            if(input.get(0).equals(closings.get(type))) {
                input.remove(0);
                break;
            }

            try {

                Optional.ofNullable(parseLine(input)).ifPresent(elem -> chunk.getChildren().add(elem));
            } catch (IllegalStateException e) {
                throw new IllegalStateException(e.getMessage() + closings.get(type));
            }
        }
        
        return chunk;
        
    }

    public static class Chunk {
        public Chunk(List<Chunk> children, String type) {
            this.children = children;
            this.type = type;
        }

        public List<Chunk> getChildren() {
            return children;
        }

        public void setChildren(List<Chunk> children) {
            this.children = children;
        }

        public String getType() {
            return type;
        }

        public void setType(String type) {
            this.type = type;
        }

        List<Chunk> children;
        String type;
    }

